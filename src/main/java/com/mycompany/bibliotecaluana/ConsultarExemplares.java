    private void listaValores() {
    try {
        BibliotecaDAO bibliteca = new BibliotecaDAO();
        DefaultTableModel tabelaModelo = (DefaultTableModel) tabelaLivros.getModel();
        tabelaModelo.setNumRows(0);
        List<Livros> lista = new ArrayList<>();
        lista = bibliteca.lerArquivo();
        for (int numLinha = 0; numLinha <lista.size(); numLinha++) {
            tabelaModelo.addRow(new Object[] {
                lista.get(numLinha).getNumeroIdentificacao(),
                lista.get(numLinha).getTitulo(),
                lista.get(numLinha).getAutor(),
                lista.get(numLinha).getAnoPublicacao(),
              
            });
        }
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(null,"A consulta ao BD pelo sistama apresentou o seginte" + erro);
    }
        }
