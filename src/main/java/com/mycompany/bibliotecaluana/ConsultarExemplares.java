    private void btnEmprestimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmprestimoActionPerformed
      
        int linhaSelecionada = tabelaLivros.getSelectedRow();
        String id;
        String Titulo;
        String Autor;
        String Ano;
       
        id = (tabelaLivros.getModel().getValueAt(linhaSelecionada, 0).toString());
        Ano = (tabelaLivros.getModel().getValueAt(linhaSelecionada, 1).toString());
        Autor = (tabelaLivros.getModel().getValueAt(linhaSelecionada, 2).toString());
        Titulo = (tabelaLivros.getModel().getValueAt(linhaSelecionada, 3).toString());
       
        Livros livros = new Livros();
        
        livros.setNumeroIdentificacao(id);
        livros.setAutor(Autor);
         livros.setTitulo(Titulo);
         livros.setAnoPublicacao(Ano);
        
        
       RealizarEmprestimo frame = new RealizarEmprestimo(Autor, Titulo, Ano, id);
        frame.setVisible(true);
    }//GEN-LAST:event_btnEmprestimoActionPerformed

